// Strings: text data enclosed in "" or ''
var firstName = "John";
console.log("First name:", firstName);
console.log("Data Type of First Name:", typeof firstName);

// typeof: determine the data type of a variable
// Number: numeric value (30, -30, 3.14)
let age = 22;
console.log("Age:", age);
console.log("Data Type of Age:", typeof age);

// booleans: either true or false
let isStudent = true;
console.log("Is John a student?", isStudent);
console.log("Data Type of isStudent:", typeof isStudent);

// null: intentional absense of value
let car = null;
console.log("Car:", car);
console.log("Data Type of car:", typeof car);
car = "AE36";
console.log("Car:", car);
console.log("Data Type of car:", typeof car);

// undefined: unintentional absence of value
let city;
console.log("City:", city);
console.log("Data Type of City:", typeof city);
